function check() {
    let kunTunColor = document.getElementById('show').style.background;
    if (kunTunColor == '#333') {
        document.getElementById('show').style.background = 'white';
        document.getElementById('show').style.color = '#333';
    } else{
        document.getElementById('show').style.background = '#333';
        document.getElementById('show').style.color = 'white';
    }
}

